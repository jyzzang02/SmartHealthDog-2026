----------------------------------------------------------
-- 1. Languages Table Enhancement
-- Adds a 'native_name' column to the existing 'languages' table
-- to better support localization.
----------------------------------------------------------
DROP TABLE IF EXISTS language;

CREATE TABLE languages (
    id integer generated by default as identity,
    name VARCHAR(255) NOT NULL, -- English name (e.g., 'Korean')
    native_name VARCHAR(255) NOT NULL, -- Native name (e.g., '한국어')
    code VARCHAR(128) NOT NULL, -- ISO code (e.g., 'ko')
    CONSTRAINT uq_languages_code UNIQUE (code),
    CONSTRAINT uq_languages_name UNIQUE (name),
    primary key (id)
);

-- Insert the base language (Korean) since your data is currently in Korean.
-- We can add English later in the translation data.
INSERT INTO languages (code, name, native_name) VALUES
('ko', 'Korean', '한국어')
ON CONFLICT (code) DO NOTHING;

INSERT INTO languages (name, native_name, code) VALUES
('English', 'English', 'en')
ON CONFLICT (code) DO NOTHING;
